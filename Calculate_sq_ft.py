# Allow user input for Length and set to a variable
Length = float(input("Please enter length of room "))

# Allow user input for Width and set to a variable
Width = float(input("Please enter width of room "))

# Set Length X Width to a new variable
SquareFoot = (Length*Width)

# Print a message that the Square Footage of the room is: Square Foot Variable
print("The square footage of the room is", SquareFoot)